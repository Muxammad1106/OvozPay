echo ""
echo "‚ö†Ô∏è  –í–ê–ñ–ù–û: –ü–µ—Ä–µ–∑–∞–π–¥–∏—Ç–µ –≤ —Å–∏—Å—Ç–µ–º—É –∏–ª–∏ –≤—ã–ø–æ–ª–Ω–∏—Ç–µ:"
echo "   newgrp docker"
echo ""
